package com.FCAI.GPA;

import android.view.View;

public class Vie {
    View vi;
    int place;

    public Vie(View vi, int place) {
        this.vi = vi;
        this.place = place;
    }

    public View getVi() {
        return vi;
    }

    public int getPlace() {
        return place;
    }

}
